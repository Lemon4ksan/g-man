package main

import (
	"bufio"
	"errors"
	"flag"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"sort"
	"strings"
)

const (
	protoPkgPath     = "go-steam-next/protocol/protobuf/"
	steamLangURL     = "https://api.github.com/repos/SteamRE/SteamKit/contents/Resources/SteamLanguage"
	protoSteamOutput = "../generated/protobuf/steam"
	steamLangOutput  = "../generated/lang/enums_generated.go"
)

var printCommands bool

func main() {
	p := flag.Bool("print_commands", false, "")
	flag.Parse()
	printCommands = *p

	args := strings.Join(os.Args[1:], " ")

	if strings.Contains(args, "proto") {
		buildProto()
	}

	if strings.Contains(args, "steamlang") {
		buildSteamLang()
	}

	if strings.Contains(args, "format") {
		formatOutput()
	}
}

func buildProto() {
	buildProtoMap("./steam", protoSteamOutput, "steam")
}

func buildProtoMap(srcDir string, outDir string, name string) {
	_ = os.MkdirAll(outDir, os.ModePerm)

	files, err := filepath.Glob(filepath.Join("Protobufs", srcDir, "*.proto"))
	if err != nil {
		fmt.Printf("Error scanning files: %v\n", err)
		return
	}

	var opt []string
	for _, proto := range files {
		base := filepath.Base(proto)
		opt = append(opt, "--go_opt=M"+base+"="+protoPkgPath+name)
	}

	for _, proto := range files {
		base := filepath.Base(proto)
		out := filepath.Join(outDir, strings.Replace(base, ".proto", ".pb.go", 1))
		fmt.Printf("# Building Proto: %s\n", out)
		compileProto("protobufs", srcDir, base, out, opt)
	}
}

func compileProto(srcBase, srcSubdir, proto, target string, opt []string) {
	outDir, _ := filepath.Split(target)
	args := []string{
		"-I=" + srcBase,
		"-I=" + srcBase + "/" + srcSubdir,
		"--go_out=" + outDir,
		"--go_opt=paths=source_relative",
	}
	args = append(args, opt...)
	args = append(args, proto)
	execute("protoc", args)
}

func buildSteamLang() {
	fmt.Println("# Fetching SteamLanguage files...")
	files, err := fetchFileList(steamLangURL)
	if err != nil {
		fmt.Printf("Error: %v\n", err)
		return
	}

	var allEnums []*EnumDef
	for _, file := range files {
		if !strings.HasSuffix(file.Name, ".steamd") {
			continue
		}
		fmt.Printf("# Parsing %s\n", file.Name)
		content, _ := downloadFile(file.DownloadURL)
		allEnums = append(allEnums, parseSteamd(content)...)
	}

	generateEnumsFile(allEnums, steamLangOutput)
}

func cleanName(eName, vName string) string {
	if prefix, ok := enumsWithDifferentPrefixes[eName]; ok {
		if after, ok0 := strings.CutPrefix(vName, prefix); ok0 {
			return after
		}
	}
	vName = strings.TrimPrefix(vName, "k_"+eName+"_")
	vName = strings.TrimPrefix(vName, "k_")
	return vName
}

func generateEnumsFile(enums []*EnumDef, target string) {
	_ = os.MkdirAll(filepath.Dir(target), os.ModePerm)
	f, _ := os.Create(target)
	defer f.Close()

	sort.Slice(enums, func(i, j int) bool {
		return enums[i].Name < enums[j].Name
	})

	w := bufio.NewWriter(f)
	fmt.Fprint(w, "// Code generated by gen tool. DO NOT EDIT.\npackage protocol\n\nimport \"fmt\"\n\n")

	for _, enum := range enums {
		if len(enum.Values) == 0 {
			continue
		}

		fmt.Fprintf(w, "type %s %s\n\nconst (\n", enum.Name, enum.Type)

		nameToValue := make(map[string]string)
		seenConstNames := make(map[string]bool)
		var orderedValues []EnumValue

		for _, val := range enum.Values {
			fullName := fmt.Sprintf("%s_%s", enum.Name, val.Name)

			if seenConstNames[fullName] {
				continue
			}
			seenConstNames[fullName] = true

			orderedValues = append(orderedValues, val)
			goValue := formatValue(enum.Name, enum.Type, val.Value)

			resolvedValue := val.Value

			if !isNumeric(val.Value) && !strings.Contains(val.Value, "|") {
				if val, ok := nameToValue[val.Value]; ok {
					resolvedValue = val
				}
			}
			nameToValue[val.Name] = resolvedValue

			commentStr := ""
			if val.Comment != "" {
				cleanC := strings.TrimPrefix(val.Comment, "//")
				cleanC = strings.TrimSpace(strings.TrimPrefix(cleanC, " "))
				commentStr = " // " + cleanC
			}
			fmt.Fprintf(w, "\t%s %s = %s%s\n", fullName, enum.Name, goValue, commentStr)
		}
		fmt.Fprint(w, ")\n\n")

		fmt.Fprintf(w, "var %s_name = map[%s]string{\n", enum.Name, enum.Name)

		seenMapKeys := make(map[string]bool)

		for _, v := range orderedValues {
			if strings.Contains(v.Value, "|") {
				continue
			}

			actualNumVal := nameToValue[v.Name]
			if !seenMapKeys[actualNumVal] {
				fullName := fmt.Sprintf("%s_%s", enum.Name, v.Name)
				fmt.Fprintf(w, "\t%s: \"%s\",\n", fullName, v.Name)
				seenMapKeys[actualNumVal] = true
			}
		}
		fmt.Fprintf(w, "}\n\n")

		fmt.Fprintf(w, "func (x %s) String() string {\n", enum.Name)
		fmt.Fprintf(w, "\tif name, ok := %s_name[x]; ok {\n", enum.Name)
		fmt.Fprintf(w, "\t\treturn name\n")
		fmt.Fprintf(w, "\t}\n")
		fmt.Fprintf(w, "\treturn fmt.Sprintf(\"%%d\", x)\n")
		fmt.Fprintf(w, "}\n\n")
	}
	w.Flush()
}

func formatOutput() {
	if fileExists(protoSteamOutput) {
		execute("go", []string{"fmt", protoSteamOutput})
	}
	if fileExists(steamLangOutput) {
		execute("go", []string{"fmt", steamLangOutput})
	}
}

func fileExists(path string) bool {
	_, err := os.Stat(path)
	if err == nil {
		return true
	}
	if errors.Is(err, os.ErrNotExist) {
		return false
	}
	return false
}

func execute(command string, args []string) {
	if printCommands {
		fmt.Println(command + " " + strings.Join(args, " "))
	}
	cmd := exec.Command(command, args...)
	err := cmd.Run()
	if err != nil {
		os.Stderr.WriteString(err.Error() + "\n")
		os.Exit(1)
	}
}
