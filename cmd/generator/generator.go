const (
	moduleRoot = "github.com/lemon4ksan/g-man"

	steamImportPath = moduleRoot + "/pkg/steam/protobuf"
	tf2ImportPath   = moduleRoot + "/pkg/tf2/protobuf"

	steamLangURL = "https://api.github.com/repos/SteamRE/SteamKit/contents/Resources/SteamLanguage"

	protoSteamOutput = "../generated/protobuf/steam"
	protoTF2Output   = "../generated/protobuf/tf2"
	steamLangOutput  = "../generated/lang/enums_generated.go"
)

var printCommands bool

func main() {
	p := flag.Bool("print_commands", false, "")
	flag.Parse()
	printCommands = *p

	args := strings.Join(os.Args[1:], " ")

	if strings.Contains(args, "proto") {
		buildProto()
	}

	if strings.Contains(args, "steamlang") {
		buildSteamLang()
	}

	if strings.Contains(args, "format") {
		formatOutput()
	}
}

func buildProto() {
	fmt.Println("### Building Steam Protobufs...")
	steamSrc, _ := filepath.Abs(filepath.Join("Protobufs", "steam"))
	steamOut, _ := filepath.Abs(protoSteamOutput)
	_ = os.MkdirAll(steamOut, os.ModePerm)

	steamFiles, _ := filepath.Glob(filepath.Join(steamSrc, "*.proto"))
	var steamMappings []string
	var steamFileNames []string
	for _, f := range steamFiles {
		base := filepath.Base(f)
		steamMappings = append(steamMappings, "--go_opt=M"+base+"="+steamImportPath)
		steamFileNames = append(steamFileNames, base)
	}

	executeInDir(steamSrc, "protoc", append([]string{
		"-I=.",
		"--go_out=" + steamOut,
		"--go_opt=paths=source_relative",
	}, append(steamMappings, steamFileNames...)...))

	fmt.Println("### Building TF2 Protobufs...")
	tempDir, _ := os.MkdirTemp("", "tf2proto_build")
	defer os.RemoveAll(tempDir)

	tf2Sandbox := filepath.Join(tempDir, "tf2_gc")
	steamSandbox := filepath.Join(tempDir, "steam")
	_ = os.MkdirAll(tf2Sandbox, os.ModePerm)
	_ = os.MkdirAll(steamSandbox, os.ModePerm)

	tf2Files, _ := filepath.Glob(filepath.Join("Protobufs", "tf2", "*.proto"))
	for _, f := range tf2Files {
		dst := filepath.Join(tf2Sandbox, filepath.Base(f))
		copySanitizeTF2(f, dst, "tf2_gc")
	}

	steamFiles, _ = filepath.Glob(filepath.Join("Protobufs", "steam", "*.proto"))
	for _, f := range steamFiles {
		copyFile(f, filepath.Join(steamSandbox, filepath.Base(f)))
	}

	tf2Out, _ := filepath.Abs(protoTF2Output)
	_ = os.MkdirAll(tf2Out, os.ModePerm)

	var mappings []string

	for _, f := range steamFiles {
		base := filepath.Base(f)
		mappings = append(mappings, "--go_opt=M"+base+"="+steamImportPath)
	}

	for _, f := range tf2Files {
		base := filepath.Base(f)

		mappings = append(mappings, "--go_opt=Mtf2_gc/"+base+"="+tf2ImportPath)
		mappings = append(mappings, "--go_opt=M"+base+"="+tf2ImportPath)
	}

	blacklist := map[string]bool{
		"steammessages.proto":              true,
		"steammessages_base.proto":         true,
		"steammessages_unified_base.proto": true,
		"enums_clientserver.proto":         true,
	}

	for _, f := range tf2Files {
		base := filepath.Base(f)
		if blacklist[base] {
			continue
		}

		fmt.Printf("# Compiling: %s\n", base)
		executeInDir(tempDir, "protoc", append([]string{
			"-I=.",
			"-I=tf2_gc",
			"-I=steam",
			"--go_out=" + tf2Out,
			"--go_opt=paths=source_relative",
		}, append(mappings, filepath.Join("tf2_gc", base))...))
	}
}

func buildSteamLang() {
	fmt.Println("# Fetching SteamLanguage files...")
	files, err := fetchFileList(steamLangURL)
	if err != nil {
		fmt.Printf("Error: %v\n", err)
		return
	}

	var allEnums []*EnumDef
	for _, file := range files {
		if !strings.HasSuffix(file.Name, ".steamd") {
			continue
		}
		fmt.Printf("# Parsing %s\n", file.Name)
		content, _ := downloadFile(file.DownloadURL)
		allEnums = append(allEnums, parseSteamd(content)...)
	}

	generateEnumsFile(allEnums, steamLangOutput)
}

func cleanName(eName, vName string) string {
	if prefix, ok := enumsWithDifferentPrefixes[eName]; ok {
		if after, ok0 := strings.CutPrefix(vName, prefix); ok0 {
			return after
		}
	}
	vName = strings.TrimPrefix(vName, "k_"+eName+"_")
	vName = strings.TrimPrefix(vName, "k_")
	return vName
}

func generateEnumsFile(enums []*EnumDef, target string) {
	_ = os.MkdirAll(filepath.Dir(target), os.ModePerm)
	f, _ := os.Create(target)
	defer f.Close()

	sort.Slice(enums, func(i, j int) bool {
		return enums[i].Name < enums[j].Name
	})

	w := bufio.NewWriter(f)
	fmt.Fprint(w, "// Code generated by gen tool. DO NOT EDIT.\npackage protocol\n\nimport \"fmt\"\n\n")

	for _, enum := range enums {
		if len(enum.Values) == 0 {
			continue
		}

		fmt.Fprintf(w, "type %s %s\n\nconst (\n", enum.Name, enum.Type)

		nameToValue := make(map[string]string)
		seenConstNames := make(map[string]bool)
		var orderedValues []EnumValue

		for _, val := range enum.Values {
			fullName := fmt.Sprintf("%s_%s", enum.Name, val.Name)

			if seenConstNames[fullName] {
				continue
			}
			seenConstNames[fullName] = true

			orderedValues = append(orderedValues, val)
			goValue := formatValue(enum.Name, enum.Type, val.Value)

			resolvedValue := val.Value

			if !isNumeric(val.Value) && !strings.Contains(val.Value, "|") {
				if val, ok := nameToValue[val.Value]; ok {
					resolvedValue = val
				}
			}
			nameToValue[val.Name] = resolvedValue

			commentStr := ""
			if val.Comment != "" {
				cleanC := strings.TrimPrefix(val.Comment, "//")
				cleanC = strings.TrimSpace(strings.TrimPrefix(cleanC, " "))
				commentStr = " // " + cleanC
			}
			fmt.Fprintf(w, "\t%s %s = %s%s\n", fullName, enum.Name, goValue, commentStr)
		}
		fmt.Fprint(w, ")\n\n")

		fmt.Fprintf(w, "var %s_name = map[%s]string{\n", enum.Name, enum.Name)

		seenMapKeys := make(map[string]bool)

		for _, v := range orderedValues {
			if strings.Contains(v.Value, "|") {
				continue
			}

			actualNumVal := nameToValue[v.Name]
			if !seenMapKeys[actualNumVal] {
				fullName := fmt.Sprintf("%s_%s", enum.Name, v.Name)
				fmt.Fprintf(w, "\t%s: \"%s\",\n", fullName, v.Name)
				seenMapKeys[actualNumVal] = true
			}
		}
		fmt.Fprintf(w, "}\n\n")

		fmt.Fprintf(w, "func (x %s) String() string {\n", enum.Name)
		fmt.Fprintf(w, "\tif name, ok := %s_name[x]; ok {\n", enum.Name)
		fmt.Fprintf(w, "\t\treturn name\n")
		fmt.Fprintf(w, "\t}\n")
		fmt.Fprintf(w, "\treturn fmt.Sprintf(\"%%d\", x)\n")
		fmt.Fprintf(w, "}\n\n")
	}
	w.Flush()
}

func formatOutput() {
	if fileExists(protoSteamOutput) {
		execute("go", []string{"fmt", protoSteamOutput})
	}
	if fileExists(steamLangOutput) {
		execute("go", []string{"fmt", steamLangOutput})
	}
}

func fileExists(path string) bool {
	_, err := os.Stat(path)
	if err == nil {
		return true
	}
	if errors.Is(err, os.ErrNotExist) {
		return false
	}
	return false
}

func execute(command string, args []string) {
	if printCommands {
		fmt.Println(command + " " + strings.Join(args, " "))
	}
	cmd := exec.Command(command, args...)

	output, err := cmd.CombinedOutput()
	if err != nil {
		fmt.Printf("Error executing %s:\n%s\n", command, string(output))
		os.Exit(1)
	}
}

func executeInDir(dir string, command string, args []string) {
	if printCommands {
		fmt.Printf("cd %s && %s %s\n", dir, command, strings.Join(args, " "))
	}
	cmd := exec.Command(command, args...)
	cmd.Dir = dir

	output, err := cmd.CombinedOutput()
	if err != nil {
		fmt.Printf("Error executing %s in %s:\n%s\n", command, dir, string(output))
		os.Exit(1)
	}
}

func copyFile(src, dst string) {
	data, err := os.ReadFile(src)
	if err != nil {
		fmt.Printf("Failed to read %s: %v\n", src, err)
		os.Exit(1)
	}
	err = os.WriteFile(dst, data, 0644)
	if err != nil {
		fmt.Printf("Failed to write %s: %v\n", dst, err)
		os.Exit(1)
	}
}

func copySanitizeTF2(src, dst, newPkg string) {
	data, err := os.ReadFile(src)
	if err != nil {
		fmt.Printf("Failed to read %s: %v\n", src, err)
		os.Exit(1)
	}
	content := string(data)

	rePkg := regexp.MustCompile(`(?m)^\s*package\s+[^;]+;`)
	if rePkg.MatchString(content) {
		content = rePkg.ReplaceAllString(content, "package "+newPkg+";")
	} else {
		reSyntax := regexp.MustCompile(`(?m)^\s*syntax\s*=\s*"[^"]+"\s*;`)
		if reSyntax.MatchString(content) {
			content = reSyntax.ReplaceAllString(content, "$0\n\npackage "+newPkg+";")
		} else {
			content = "package " + newPkg + ";\n\n" + content
		}
	}

	reLeadingDot := regexp.MustCompile(`([\s\(\<])\.([a-zA-Z])`)

	content = reLeadingDot.ReplaceAllStringFunc(content, func(m string) string {
		if strings.Contains(m, ".google") {
			return m
		}
		return string(m[0]) + string(m[2:])
	})

	err = os.WriteFile(dst, []byte(content), 0644)
	if err != nil {
		fmt.Printf("Failed to write sanitized %s: %v\n", dst, err)
		os.Exit(1)
	}
}
